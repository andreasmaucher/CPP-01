#include "Zombie.hpp"

Zombie* zombieHorde( int N, std::string name )
{
	Zombie *horde = new Zombie[N];
	int i = 0;
	
	while (i < N)
	{
		std::string name = "zombie " + std::to_string(i);
		horde[i].nameZombie(name);
		i++;
	}
	return(horde);
}
